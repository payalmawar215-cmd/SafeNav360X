const RTL_LANGS = ['ar'];

const LanguageContext = createContext();

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState(localStorage.getItem('safenav_lang') || 'en');

  const setLanguage = (l) => {
    setLang(l);
    localStorage.setItem('safenav_lang', l);
    document.documentElement.dir = RTL_LANGS.includes(l) ? 'rtl' : 'ltr';
  };

  const t = (key) => {
    return translations[lang]?.[key] || translations.en[key] || key;
  };

  const isRTL = RTL_LANGS.includes(lang);

  return (
    <LanguageContext.Provider value={{ lang, setLanguage, t, isRTL }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  return useContext(LanguageContext);
}

